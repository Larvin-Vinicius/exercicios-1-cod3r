function diaUtil(dia) {
    switch (dia) {
        case 1:
            console.log('Domingo, não é dia util');
            break;
        case 2:
            console.log('Segunda, é dia util');
            break;
        case 3:
            console.log('Terça, é dia util');
            break;
        case 4:
            console.log('Quarta, é dia util');
            break;
        case 5:
            console.log('Quinta, é dia util');
            break;
        case 6:
            console.log('Sexta, é dia util');
            break;
        case 7:
            console.log('Sabado, não é dia util');
        break;
    }
}

diaUtil(1);
diaUtil(2)
diaUtil(3)
diaUtil(4)
diaUtil(5)
diaUtil(6)
diaUtil(7)
